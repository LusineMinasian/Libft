#include "libft.h"

int	ft_isalnum(const char c)
{
	if (c <= '9' && c >= '0')
	{
		return (1);
		if ((c <= 'a' && c >= 'z') || (c <= 'A' && c >= 'Z'))
			return (1);
	}
	return (0);
}
